package java_operators;

public class AllArithmetics {

    public static void main(String[] args) {

        float A = 20;
        float B = 15;

        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);
        System.out.println(A % B);

    }
}
